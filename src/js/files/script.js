function filterCatalogItems() {
   // Get all filter buttons
   const filterButtons = document.querySelectorAll('.filter__button')

   // Get catalog items only within the element with id "catalog"
   const catalogItems = document.querySelectorAll('#catalog .catalog__item')

   // Function to set active class
   function setActiveButton(button) {
      filterButtons.forEach(btn => btn.classList.remove('_active')) // Remove _active from all buttons
      button.classList.add('_active') // Add _active to the clicked button
   }

   // Function to filter items
   function filterItems(filterCategory) {
      catalogItems.forEach(item => {
         const itemCategories = item.querySelector('.catalog__category')
            .textContent.split(',')
            .map(cat => cat.trim())

         if (filterCategory === 'Все' || itemCategories.includes(filterCategory)) {
            item.classList.remove('hidden') // Show item by removing the "hidden" class
         } else {
            item.classList.add('hidden') // Hide item by adding the "hidden" class
         }
      })
   }

   // Add click event listener to each filter button
   filterButtons.forEach(button => {
      button.addEventListener('click', function () {
         const filterCategory = button.textContent.trim()
         setActiveButton(button)
         filterItems(filterCategory)
      })
   })

   // Set "Все" as the default active button and display all items initially
   const allButton = Array.from(filterButtons).find(btn => btn.textContent.trim() === 'Все')
   if (allButton) {
      setActiveButton(allButton)
      filterItems('Все')
   }
}

function handleReviews() {
   const reviewsContainer = document.querySelector('.reviews__items')
   const reviews = reviewsContainer.querySelectorAll('.reviews__item')
   const showMoreButton = document.querySelector('.reviews__show-more')

   let visibleReviewsCount = 5
   const increment = 10

   // Initially hide all reviews except the first 5
   reviews.forEach((review, index) => {
      if (index >= visibleReviewsCount) {
         review.classList.add('hidden') // Hide review by adding the "hidden" class
      }
   })

   showMoreButton.addEventListener('click', function () {
      if (visibleReviewsCount < reviews.length) {
         visibleReviewsCount += increment
         reviews.forEach((review, index) => {
            if (index < visibleReviewsCount) {
               review.classList.remove('hidden') // Show review by removing the "hidden" class
            }
         })

         if (visibleReviewsCount >= reviews.length) {
            showMoreButton.textContent = 'Скрыть отзывы'
         }
      } else {
         // Hide all reviews except the first 5
         visibleReviewsCount = 5
         reviews.forEach((review, index) => {
            if (index >= visibleReviewsCount) {
               review.classList.add('hidden') // Hide review by adding the "hidden" class
            }
         })
         showMoreButton.textContent = 'Показать ещё'
      }
   })
}

