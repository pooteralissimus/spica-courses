function switchLanguage() {
   currentLanguage = currentLanguage === 'ru' ? 'ua' : 'ru'
   updateContent()
   localStorage.setItem('language', currentLanguage)
   updateMetaTags()
}

function updateContent() {
   document.querySelectorAll('[data-translate]').forEach(element => {
      const key = element.getAttribute('data-translate')
      if (translations[currentLanguage][key]) {
         element.textContent = translations[currentLanguage][key]
      }
   })

   document.querySelector('[data-language-switcher]').textContent = currentLanguage === 'ru' ? 'RU | UA' : 'UA | RU'

   // Update other dynamic content that needs translation
   updateCatalogItems()
   updatePackages()
   updatePopups()
}

function updateCatalogItems() {
   // Update catalog item titles, descriptions, and buttons
   document.querySelectorAll('.catalog__item').forEach(item => {
      const titleKey = item.querySelector('.catalog__title h3').getAttribute('data-translate')
      if (translations[currentLanguage][titleKey]) {
         item.querySelector('.catalog__title h3').textContent = translations[currentLanguage][titleKey]
      }

      const buttonKey = item.querySelector('.catalog__button').getAttribute('data-translate')
      if (translations[currentLanguage][buttonKey]) {
         item.querySelector('.catalog__button').textContent = translations[currentLanguage][buttonKey]
      }
   })
}

